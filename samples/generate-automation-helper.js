const fs = require("fs");
const fsPromises = require("fs/promises");
const os = require("os");

var targetDir = os.homedir() + "/.config/augene-ng"

main();

function main() {

    fs.readFile(targetDir + "/plugin-metadata.json",  {}, (err, data) => {
        if (err)
            console.log(err);
        else {
            data = JSON.parse(data);
            fsPromises.mkdir(targetDir + "/audio-plugins", {"recursive": true});
            data.forEach(plugin => {
                generatePluginMacro(plugin);
            });
        }
    });

}

function escapeName(s) {
    return s.toUpperCase()
        .replaceAll(/(\p{S}|\p{M}|\p{P}|\p{Z})/gu, "_")
        .replaceAll(/([0-9])/g, "\\$1")
        .replaceAll(' ', '_')
        .replaceAll("\\_", "_")
	;
}

function generatePluginMacro(plugin) {
    var name = escapeName(plugin.name);
    var uniqueId = plugin["unique-id"];
    var fsname = name.replaceAll("\\", "");

    var s = "// generated by generate-automation-helper.js\n" +
        "#macro AUDIO_PLUGIN_USE nameLen:number, ident:string {  __MIDI #F0, #7D, \"augene-ng\", $nameLen, $ident, #F7 }\n" +
        "#macro AUDIO_PLUGIN_PARAMETER parameterID:number, val:number { \\\n" +
        "    __MIDI #F0, #7D, \"augene-ng\", 0, \\\n" +
        "    $parameterID % #80, $parameterID / #80, $val % #80, $val / #80 } \n" +
        "\n" +
        "#macro " + name + " { AUDIO_PLUGIN_USE " + uniqueId.length + ", \"" + uniqueId + "\" }\n";
    plugin.parameters.forEach(para => {
        if (para.name.match(/MIDI CC [0-9]+\|[0-9]+/))
            return;
        var paraName = escapeName(para.name);
        s += "#macro " + name + "_" + paraName + " val { AUDIO_PLUGIN_PARAMETER " + para.index + ", $val }\n";
    });
    fs.writeFile(targetDir + "/audio-plugins/" + plugin.type + "_" + fsname + ".mugene", s, {}, (err) => {
        if (err != null)
            console.log(err);
    });
}
